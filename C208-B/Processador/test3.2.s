# GRAVA O DADO "CAFE" NO END. BASE 0X120 DESLOCADO DE 6 POSIÇÕES:
addiu $1,$0,0x120
addiu $2,$0,0xCAFE
sw $2,6($1)

# GRAVA O DADO "B0B0" NO END. BASE 0X44 DESLOCADO DE 8 POSIÇÕES:
addiu $5,$0,0x44
addiu $2,$0,0xB0B0
sw $2,8($5)

# CARREGA O DADO CAFE EM $t3, O DADO B0B0 EM $t4
lw $3,6($1)
lw $4,8($5)

# SOMAR OS DOIS DADOS
addu $6,$3,$4

# GUARDAR O RESULTADO NO END. 02A0 DESLOCADO DE 4 POSIÇÕES
addiu $7,$0,0x02A0
sw $6, 4($7)